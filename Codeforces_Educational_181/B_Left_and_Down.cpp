void solve(int tt)
{
    ll a, b, k;
    cin >> a >> b >> k;

    ll gc = gcd(a, b);
    ll dx = a / gc;
    ll dy = b / gc;

    if(max(dx, dy) <= k)
        cout << 1 << nl;
    else
        cout << 2 << nl;
}

int main()
{
    FAST;
    int tc = 1;
    cin >> tc;
    for (int tt = 1; tt <= tc; tt++)
    {
        solve(tt);
    }

    return 0;
}
